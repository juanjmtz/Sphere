    private void quantityTextFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_quantityTextFieldKeyTyped
        // TODO add your handling code here:
        char teclaPulsada = evt.getKeyChar();
        if (!Character.isDigit(teclaPulsada)) {
            evt.consume();
        }
        if (quantityTextField.getText().length() == 10) {
            evt.consume();
        }
        if (!"".equals(quantityTextField.getText())) {
            if (Integer.parseInt(quantityTextField.getText() + "") > 20000) {
                quantityTextField.setText("20000");
                evt.consume();
            }
        }
    }//GEN-LAST:event_quantityTextFieldKeyTyped

