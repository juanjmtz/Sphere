    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {

            if (MDBMV.printBytes.isSelected() == true) {
                UnicastBMVYesBytes.ResendRequest((groupTextField.getText()), firstMessageTextField.getText(), quantityTextField.getText());
            } else {
                UnicastBMVNoBytes.ResendRequest((groupTextField.getText()), firstMessageTextField.getText(), quantityTextField.getText());
            }

            this.dispose();
        } catch (IOException e) {
            //System.out.print("Exception sending logoff packet: " + e);
            String messageError = "Socket closed" + "\n" + e;

            // create a jframe
            JFrame error = new JFrame();

            // show a joptionpane dialog using showMessageDialog
            JOptionPane.showMessageDialog(error, messageError, "Information", JOptionPane.INFORMATION_MESSAGE);

        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void groupTextFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_groupTextFieldKeyTyped
        // TODO add your handling code here:
        char teclaPulsada = evt.getKeyChar();
        if (!Character.isDigit(teclaPulsada)) {
            evt.consume();
        }
        if (groupTextField.getText().length() == 2) {
            evt.consume();
        }
    }//GEN-LAST:event_groupTextFieldKeyTyped

